function mkcd {
  param(
    [Parameter(Mandatory = $true, Position = 0)]
    [string]
    $Path
  )

  if (Test-Path -Path $Path) {
    if (Test-Path -PathType Container $Path) {
      Write-Host "Directory '$Path' already exists."
      Set-Location -Path $Path
    } else {
        Write-Error "Path '$Path' exists but is not a directory."
        return
    }
  } else {
    try {
      New-Item -ItemType Directory -Path $Path -Force | Out-Null # -Force creates parent directories
      Set-Location -Path $Path
    }
    catch {
      Write-Error "Error creating directory '$Path': $($_.Exception.Message)"
      return
    }
  }
}

function Get-Head {
    param(
        [Alias('n')]
        [int]$Lines = 5,
        [Parameter(ValueFromRemainingArguments)]
        [string[]]$Path
    )

    if ($Path -eq $null -or $Path.Count -eq 0) {
        Write-Error "You must specify a path."
    }

    foreach ($file in $Path) {
        Get-Content -Path $file -Head $Lines
    }
}

function Get-Tail {
    param(
        [Alias('n')]
        [int]$Lines = 5,
        [Parameter(ValueFromRemainingArguments)]
        [string[]]$Path
    )

    if ($Path -eq $null -or $Path.Count -eq 0) {
        Write-Error "You must specify a path."
    }

    foreach ($file in $Path) {
        Get-Content -Path $file -Tail $Lines
    }
}

